def check_if_word_is_valid(word):
  # Check for the avlidity of the input word
  if word is None:
    print('No word provided')
    return (False)
  elif isinstance(word, str) == False:
    print('Not a string')
    return (False)
  elif len(word) == 0:
    print('Empty string')
    return (False)
  elif word.isalpha() == False:
    print('String with non-alphabets')
    return (False)
  else:
    return True


