# Iterate through each letter in the string to check if it is upper or lower case and increment the counts accordingly
def count_upper_case_and_lower_case_characters(input_string):
  if check_if_word_is_valid(input_string) == True:
    upper, lower = 0, 0
    for i in input_string:
      if (i >= 'a' and i <= 'z'):
        lower = lower + 1
      elif (i >= 'A' and i <= 'Z'):
        upper = upper + 1
    
    
    print("Upper case count:")
    print(upper)
    print("Lower case count:")
    print(lower)

