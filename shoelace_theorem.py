# Problem 1
def area_of_polygon_using_shoelace_theorem(vertices):
    n = len(vertices) # of corners
    area = 0.0
    for i in range(n):
        j = (i + 1) % n
        area += vertices[i][0] * vertices[j][1]
        area -= vertices[j][0] * vertices[i][1]
    area = abs(area) / 2.0
    print("The area of the polygom is: ")
    print(area)

points = [(10.0, 1.0), (0.0, 5.0), (5.0, 8.0)]
area_of_polygon_using_shoelace_theorem(points)
