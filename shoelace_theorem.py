import math

def area_of_triangle():
  base = int(input("Enter triangle base: "))
  height = int(input("Enter triangle height: "))
  area_triangle = 1/2 * base * height
  print("The area of the triangle is:")
  print(area_triangle)

def area_of_circle():
  radius = int(input("Enter cricle radius: "))
  area_circle = (math.pi * radius)**2
  print("The radius of the circle is:")
  print(area_circle)

def area_of_square():
  length_of_sides = int(input("Enter length of sides of square: "))
  area_square = length_of_sides * length_of_sides
  print("The area of the square is:")
  print(area_square)

def area_of_rhombus():
  p = int(input("Enter rhombus diagonal length 1: "))
  q = int(input("Enter rhombus diagonal length 2: "))
  area_rhombus = 1/2 * (p * q)
  print("The area of the rhombus is:")
  print(area_rhombus)

def area_of_trapezoid():
  b1 = int(input("Enter trapezoid base 1: "))
  b2 = int(input("Enter trapezoid base 2: "))
  h = int(input("Enter trapezoid height: "))
  area_trapezoid = ((b1 + b2)/2) * h
  print("The area of the trapezoid is:")
  print(area_trapezoid)

